package BD;

public class CancionesLR {

    int lid, cid;

    public CancionesLR(int lid, int cid) {
        this.lid = lid;
        this.cid = cid;
    }

    public int getLid() {
        return lid;
    }

    public void setLid(int lid) {
        this.lid = lid;
    }

    public int getCid() {
        return cid;
    }

    public void setCid(int cid) {
        this.cid = cid;
    }
}
