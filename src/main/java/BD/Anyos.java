package BD;

public class Anyos {

    int yid;
    String ynumero;

    public Anyos(int yid, String ynumero) {
        this.yid = yid;
        this.ynumero = ynumero;
    }

    public int getYid() {
        return yid;
    }

    public void setYid(int yid) {
        this.yid = yid;
    }

    public String getYnumero() {
        return ynumero;
    }

    public void setYnumero(String ynumero) {
        this.ynumero = ynumero;
    }
}
